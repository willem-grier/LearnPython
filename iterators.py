# __iter__ and __next__ are required to make a class iterable
class MyNumbers:
  def __iter__(self):
    self.a = 1
    return self

  def __next__(self):
    if self.a <= 10:
      x = self.a
      self.a += 1
      return x
    else:
      raise StopIteration
    
myclass = MyNumbers()
myiter = iter(myclass)

for x in myiter:
  print(x)